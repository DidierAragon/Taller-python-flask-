#Git — Control de versiones basico

#La Diferencia de Áreas
'Área de Trabajo: Tu carpeta normal en la computadora. Modificas código aquí.'

'Repositorio: La base de datos oculta (.git) donde se guarda tu historial permanentemente.'

#Comandos Básicos


#git init
'Convierte una carpeta común en un repositorio de Git. Solo se hace una vez al iniciar el proyecto.'

#git status
'Te dice qué está pasando. Muestra qué archivos editaste y cuáles están listos para guardarse.'

#git add
'Pasa tus cambios del Área de Trabajo al "carrito de preparación" (Staging Area).'

#git commit
'Guarda permanentemente en el Repositorio los cambios que metiste al "carrito". Lleva un mensaje (-m) explicativo'

#git log
'Muestra la lista de todos los guardados (commits) que has hecho en el pasado.'

#git push 
'sube tus cambios guardados de tu computadora a internet (GitHub).'

