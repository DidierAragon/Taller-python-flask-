#Variable 
"""En Python, una variable es un contenedor o etiqueta que sirve para guardar y hacer referencia a un dato en la memoria del computador."""

"""una variable se declara usando el operador ="""

# Ejemplo de asignación de variables
nombre = "Juan"          # Variable de tipo string
edad = 25                # Variable de tipo int
precio = 19.99           # Variable de tipo float
es_activo = True        # Variable de tipo bool

print(f"Nombre: {nombre}, Edad: {edad}, Precio: ${precio}, Activo: {es_activo}")

"""el  tipado dinamico significa que no es necesario especificar el tipo de dato ya que python lo analiza automaticamente."""

