#Que es Flask y como instalarlo
"""Un framework es un entorno de trabajo que ofrece una estructura base para desarrollar aplicaciones de software, ahorrando tiempo al proporcionar herramientas y funciones comunes. Un microframework es una versión minimalista y ligera que incluye solo lo esencial, permitiéndote añadir las herramientas adicionales que necesites.
"""

#Diferencias Clave con Django
"""Django (Framework completo): Es conocido como "pilas incluidas". Trae por defecto un sistema de autenticación, ORM (mapeo objeto-relacional para bases de datos), panel de administración y estructura de directorios. Es ideal para proyectos grandes y complejos."""

"""Flask (Microframework): No incluye ORM, validación de formularios ni herramientas de base de datos predeterminadas. Es altamente flexible, rápido de aprender y perfecto para APIs, microservicios o aplicaciones web pequeñas."""

#Cómo instalar Flask con pip
"""Puedes instalar Flask y su servidor integrado ejecutando el siguiente comando en tu terminal o consola:"""

#flujo de comandos en la terminal para linux y windows

'Crear entorno virtual' 
# Windows: python -m venv venv
# Linux/Mac: python3 -m venv venv

'Activar entorno virtual' 
# Windows: venv\Scripts\activate
# Linux/Mac: source venv/bin/activate

'Instalar Flask' 
# Windows: pip install flask
# Linux/Mac: pip install flask

'Verificar instalación'
# Windows: python -m flask --version
# Linux/Mac: python3 -m flask --version